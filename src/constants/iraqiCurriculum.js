// الحصول على قائمة المراحل (متوسطة + إعدادية فقط — جامعي لا يدعم المنهج)
export function getStages() {
  return Object.keys(IRAQI_CURRICULUM).filter((s) => IRAQI_CURRICULUM[s] !== null)
}

// الحصول على قائمة الصفوف لمرحلة معينة
export function getGrades(stage) {
  const stageData = IRAQI_CURRICULUM[stage]
  if (!stageData) return []
  return Object.keys(stageData)
}

// الحصول على قائمة المواد لصف معين
export function getSubjects(stage, grade) {
  return IRAQI_CURRICULUM[stage]?.[grade] || []
}
